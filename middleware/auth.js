//Import the package JsonWebToken 
const jwt = require('jsonwebtoken');

//Routes control: check the TOKEN of the user, if it matches the id of the user in the request, it will be allowed to change the corresponding data.
module.exports = (req, res, next) => {
  try {
    const token = req.headers.authorization.split(' ')[1];
    const decodedToken = jwt.verify(token, process.env.TK_JWT);
    const userId = decodedToken.userId;
    if (req.body.userId && req.body.userId !== userId) {
      throw 'Invalid user ID';
    } else {
      next();
    }
  } catch {
    res.status(401).json({
      error: new Error('Invalid request!')
    });
  }
};